	public int getClosest(int N) {
		int lower = N;
		int upper = N;
		while (true) {
			if (check(lower)) {
				return lower;
			}
			if (check(upper)) {
				return upper;
			}

			--lower;
			++upper;
		}
	}

	boolean check(int x) {
		return isDifferent(x) && isPrime(x);
	}

	boolean isDifferent(int x) {
		return String.valueOf(x).chars().distinct().count() == String.valueOf(x).length();
	}

	boolean isPrime(int x) {
		if (x <= 1) {
			return false;
		}

		for (int i = 2; i * i <= x; ++i) {
			if (x % i == 0) {
				return false;
			}
		}

		return true;
	}

