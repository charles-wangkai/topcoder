  public int[] howManyReveals(int[] prices) {
    int[] lengths = new int[prices.length];
    int[] wayNums = new int[lengths.length];
    for (int i = 0; i < lengths.length; ++i) {
      lengths[i] = 1;
      wayNums[i] = 1;

      for (int j = 0; j < i; ++j) {
        if (prices[j] < prices[i]) {
          if (lengths[j] + 1 > lengths[i]) {
            lengths[i] = lengths[j] + 1;
            wayNums[i] = wayNums[j];
          } else if (lengths[j] + 1 == lengths[i]) {
            wayNums[i] += wayNums[j];
          }
        }
      }
    }

    int maxLength = Arrays.stream(lengths).max().getAsInt();

    return new int[] {
      maxLength,
      IntStream.range(0, wayNums.length)
          .filter(i -> lengths[i] == maxLength)
          .map(i -> wayNums[i])
          .sum()
    };
  }

